#import "acADModel.h"

@implementation acADModel

+(instancetype)adWithDict:(NSDictionary *)dict{
    
    acADModel *model = [[acADModel alloc]init];
    model.apic = dict[@"apic"];
    model.acid = dict[@"acid"];
    model.aurl = dict[@"aurl"];
    model.aname = dict[@"aname"];
    
    return model;
}

@end
