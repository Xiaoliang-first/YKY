#import "adBannerModle.h"
#import "common.h"

@implementation adBannerModle

+(instancetype)adModelWithDict:(NSDictionary *)dict{
    adBannerModle * model = [[adBannerModle alloc]init];
    model.ID = [NSString stringWithFormat:@"%@",dict[@"id"]];
    model.apic = dict[@"apic"];
    model.aurl = dict[@"aurl"];
    return model;
}


@end
