#import "noticeModel.h"

@implementation noticeModel


+(instancetype)noticeWithDict:(NSDictionary *)dict{
    
    noticeModel *notice = [[noticeModel alloc]init];
    
    notice.sysTitle = dict[@"title"];
    notice.sysContent = dict[@"content"];
    notice.publicDate = dict[@"atime"];
    notice.sysInfoId = dict[@"id"];
    
    return notice;
    
}


@end
